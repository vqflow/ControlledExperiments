select
	class.CLASS_ID,
	class.CLASS_DESC,
	class.DMN_ID,
	class.QUAL_ID,
	class.START_DTE,
	class.END_DTE,
	class.MAX_SIZE,
	class.DMN_DESC,
	class.QUAL_TITLE,
	class.COL_NUM,
	class.USER_VALUE,
	class.LABEL,
	class.USER_DESC,
	class_student.STUD_ID,
	class_student.CLASS_STAT_ID,
	class_student.COMMENTS,
	class_student.LNAME,
	class_student.FNAME,
	class_student.MI,
	class_student.CLASS_STAT_DESC,
	class_schedule.SCHD_ID,
	class_schedule.CPNT_TYP_ID,
	class_schedule.ACT_CPNT_ID,
	class_schedule.REV_DTE,
	class_schedule.TIMEZONE_ID,
	class_schedule.DISPLAY_IN_SCHD_TZ,
	class_schedule.START_DTE,
	class_schedule.END_DTE,
	class_schedule.REV_NUM
from
	(
		select
			PA_CLASS.CLASS_ID,
			PA_CLASS.CLASS_DESC,
			PA_CLASS.DMN_ID,
			PA_CLASS.QUAL_ID,
			PA_CLASS.START_DTE,
			PA_CLASS.END_DTE,
			PA_CLASS.MAX_SIZE,
			PA_DOMAIN.DMN_DESC,
			PA_QUAL.QUAL_TITLE,
			PA_CLASS_USER.COL_NUM,
			PA_CLASS_USER.USER_VALUE,
			PA_USRCL_CLASS.LABEL,
			PA_USRRF_CLASS.USER_DESC
		from
			PA_CLASS,
			PA_DOMAIN,
			PA_QUAL,
			PA_CLASS_USER,
			PA_USRCL_CLASS,
			PA_USRRF_CLASS
		where
			PA_CLASS.DMN_ID = PA_DOMAIN.DMN_ID
			AND PA_CLASS.QUAL_ID = PA_QUAL.QUAL_ID
			AND PA_CLASS.CLASS_ID = PA_CLASS_USER.CLASS_ID
			AND PA_CLASS_USER.COL_NUM = PA_USRCL_CLASS.COL_NUM
			AND PA_CLASS_USER.USER_VALUE = PA_USRRF_CLASS.USER_ID
			AND PA_CLASS_USER.COL_NUM = PA_USRRF_CLASS.COL_NUM
	) as class,
	(
		select
			PA_CLASS_STUDENT.CLASS_ID,
			PA_CLASS_STUDENT.STUD_ID,
			PA_CLASS_STUDENT.CLASS_STAT_ID,
			PA_CLASS_STUDENT.COMMENTS,
			PA_STUDENT.LNAME,
			PA_STUDENT.FNAME,
			PA_STUDENT.MI,
			PA_CLASS_STAT.CLASS_STAT_DESC
		from
			PA_CLASS_STUDENT,
			PA_STUDENT,
			PA_CLASS_STAT
		where
			PA_CLASS_STUDENT.STUD_ID = PA_STUDENT.STUD_ID
			AND PA_CLASS_STUDENT.CLASS_STAT_ID = PA_CLASS_STAT.CLASS_STAT_ID
	) as class_student,
	(
		select
			PA_CLASS_SCHED.CLASS_ID,
			PA_CLASS_SCHED.SCHD_ID,
			PA_SCHED.CPNT_TYP_ID,
			PA_SCHED.ACT_CPNT_ID,
			PA_SCHED.REV_DTE,
			PA_SCHED.TIMEZONE_ID,
			PA_SCHED.DISPLAY_IN_SCHD_TZ,
			PS_SCHD_RESOURCES.START_DTE,
			PS_SCHD_RESOURCES.END_DTE,
			PV_COURSE.REV_NUM
		from
			PA_CLASS_SCHED,
			PA_SCHED,
			PS_SCHD_RESOURCES,
			PV_COURSE
		where
			PA_CLASS_SCHED.SCHD_ID = PA_SCHED.SCHD_ID
			AND PA_CLASS_SCHED.SCHD_ID = PS_SCHD_RESOURCES.SCHD_ID
			AND PA_SCHED.CPNT_TYP_ID = PV_COURSE.CPNT_TYP_ID
			AND PA_SCHED.ACT_CPNT_ID = PV_COURSE.ACT_CPNT_ID
			AND PA_SCHED.REV_DTE = PV_COURSE.REV_DTE
	) as class_schedule
where
	class.CLASS_ID = class_student.CLASS_ID
	AND class.CLASS_ID = class_schedule.CLASS_ID